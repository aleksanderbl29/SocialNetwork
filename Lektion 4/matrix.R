n <- 23
sim_matrix <- matrix(0, nrow = n, ncol = n)
for (i in 1:n) {
  for (j in 1:n) {
    numerator <- sum(m[i, ] * m[j, ])
    denominator <- (sqrt(sum(m[i, ]^2)) * sqrt(sum(m[j, ]^2)))
    if (denominator > 0) {
      cosine <- numerator / denominator
    } else {
      cosine <- 0
    }
    sim_matrix[i, j] <- cosine
  }
}
